#include <iostream>
using namespace std;

int main()
{
    cout << "===================================== \n" << endl;
    cout << "=            RAFLI WAHYUDI          = \n" << endl;
    cout << "===================================== \n" << endl;

    cout << "=      OPERASI OPERATOR LOGIKA      = \n" << endl;

    cout << "\n TABEL KEBENARAN OPERATOR AND \n" << endl;
    cout <<" 1 && 1 =" << (1 && 1) << endl;
    cout <<" 1 && 0 =" << (1 && 0) << endl;
    cout <<" 0 && 1 =" << (0 && 1) << endl;
    cout <<" 0 && 0 =" << (0 && 0) << endl;

    cout << "\n TABEL KEBENARAN OPERATOR OR \n" << endl;
    cout <<" 1 || 1 =" << (1 || 1) << endl;
    cout <<" 1 || 0 =" << (1 || 0) << endl;
    cout <<" 0 || 1 =" << (0 || 1) << endl;
    cout <<" 0 || 0 =" << (0 || 0) << endl;

    cout << "\n TABEL KEBENARAN OPERATOR NOT \n" << endl;
    cout <<" !1 =" << !1 << endl;
    cout <<" !0 =" << !0 << endl;


    return 0;
}
